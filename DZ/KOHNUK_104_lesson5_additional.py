# задача 1

# принимаем строку текста, разбиваем на слова, слова записываем в список
words = [word for word in input("Введите строку текста: ").split()]
length = 0
# находим длину каждого слова и выводим на экран. а так же подсчитываем сумму длин всех слов
for elem in words:
    print(f"Длина слова {elem} = {len(elem)}")
    length += len(elem)
# выводим на экран сумму длин всех слов
print(f"Сумма длин всех слов в вашем предложении равна {length}")

