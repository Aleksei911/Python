# задача 4 с созданием нового массива

# принимаем строку текста, разбиваем на отдельные объекты, объекты записываем в список
elements = [elem for elem in input("Введите строку текста: ").split()]
# создаем пустой список, в который будем добавлять объекты из списка elements
new_elements = []
# проходимся циклом по объектам списка elements начиная с конца и добавляем каждый объект в конец списка new_elements
for i in range(len(elements) - 1, -1, -1):
    new_elements.append(elements[i])
# выводим результат на экран
print(new_elements)
