# задание 6 (красивый калькулятор)

for i in range(1, 10, 3):  # i - число которое будем умножать. задаем шаг 3, т.к. будет 3 столбца
    for j in range(1, 11):  # j - число от 1 до 10, НА которые мы будем умножать
        print(f"\t{i} x {j} = {i * j}".ljust(20) + f"{i + 1} x {j} = {(i + 1) * j}".ljust(
            20) + f"{i + 2} x {j} = {(i + 2) * j}")
    print()  # для красоты и наглядности)

