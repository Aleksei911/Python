numbers = []  # создаем пустой список, в который будем помещать числа ссответствующие условию
for i in range(1, 500):  # проверяем все числа от 1 до 499 включительно и добавляем в список только кратные 5
    if i % 5 == 0:
        numbers.append(i)
print(*numbers)  # выводим результат в 1 строку через пробелы

